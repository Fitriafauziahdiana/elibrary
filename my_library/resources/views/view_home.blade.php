    <h4>Pengertian</h4>
     Kata literasi bukan menjadi hal baru tentunya, bagi kita yang hidup di tengah kota dengan beragam kemudahan
     mendapatkan informasi. Akan tetapi, masih saja ada kekeliruan saat memaknai kata tersebut. Secara etimologi, 
     istilah literasi berasal dari bahasa Latin, yakni “Literatus” yang memiliki arti orang yang belajar. Istilah 
     literasi menurut Kamus Besar Bahasa Indonesia (KBBI) diartikan sebagai kemampuan menulis dan membaca. UNESCO 
     pun memberikan pemaknaan terhadap literasi yang lebih mendalam. Dikatakan bahwa literasi merupakan kemampuan 
     untuk mengidentifikasi, memahami, megartikan, menciptakan, mengomunikasikan, menghitung, menggunakan materi 
     tercetak dan tertulis yang berkaitan dengan berbagai konteks. Melalui pemaknaan tersebut, bahwa kegiatan literasi 
     tidak hanya diartikan sebagai suatu kegiatan membaca dan menulis saja, namun dengan cakupan yang lebih mendalam.
    <p>
    <h4>Peranan Budaya Literasi </h4>
    Pada penerapannya, kegiatan literasi ini pun mengalami perkembangan sesuai dengan zaman yang semakin maju. Mungkin 
    kalau dahulu saat mendengar kata “Literasi”, yang terbesit di benak kita adalah suatu kegiatan membaca buku, koran,
    majalah, dsb. Tetapi kini kata literasi sudah berkembang pesat. Adanya literasi baca tulis, literasi numerasi,
    literasi sains, literasi finansial, literasi digital, literasi agama, serta literasi budaya dan kewargaan. 
    Banyaknya macam literasi tersebut semakin memperkuat bahwa literasi memang terintegrasi pada semua kegiatan 
    yang kita lakukan dalam sehai-hari
